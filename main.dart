void main() {
  // Single LineComment
  /* Multi Line Comment */

  print('Hello'); // Print stuff
  // \n included

  // DataTypes
  int i = 1;
  print(i);

  String str = "Faisal";
  print(str);

  bool isTrue = true;
  print(isTrue);

  // Dynamic datatype -> Any type of value
  dynamic dvar = "faisal";
  print(dvar);
  dvar = 420;
  print(dvar);
}
